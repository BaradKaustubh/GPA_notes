<?php
$color = "red";
switch ($color) {
    case "red":
        echo "Color is red";
        break;
    case "blue": 
        echo "Color is blue";
        break;
    case "green":
        echo "Color is green";
        break;
    default:
        echo "Color is not red, blue or green";
}

$number = 5;
switch ($number) {
    case 1:
        echo "Number is 1";
        break;
    case 2:
        echo "Number is 2";
        break;
    case 3:
        echo "Number is 3";
        break;
    default:
        echo "Number is not 1, 2 or 3";
}
?>